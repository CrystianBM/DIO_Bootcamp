//declarando função

function ex1(){
    console.log("Primeira função - feita.");
    console.log("---------------------");
}

ex1();



// expressões de função (voce pode ou não nomear a função)

var ex2 = function(){
    console.log("Primeira expressão de função - feita.");
    console.log("---------------------");
}

ex2();



// arrow function (não pode ser nomeada)

var ex3 = () =>{
    console.log("Primeira arrow function feita.");
    console.log("---------------------");
}

ex3();
